/**
 * Plot's index in plot list tuple for series
 */
export var PlotRowValueIndex;
(function (PlotRowValueIndex) {
    PlotRowValueIndex[PlotRowValueIndex["Open"] = 0] = "Open";
    PlotRowValueIndex[PlotRowValueIndex["High"] = 1] = "High";
    PlotRowValueIndex[PlotRowValueIndex["Low"] = 2] = "Low";
    PlotRowValueIndex[PlotRowValueIndex["Close"] = 3] = "Close";
})(PlotRowValueIndex || (PlotRowValueIndex = {}));
